import pandas as pd
import numpy as np
import os

class SyntheticDataGenerator:
    """Generates synthetic candidate profiles for training the suitability classifiers."""
    def __init__(self, seed=42):
        self.seed = seed
        np.random.seed(seed)

    def generate_candidates(self, n=500):
        """
        Generate candidate suitability records.
        Features match parameters generated by resume parsed fields.
        """
        data = []
        for i in range(n):
            # Generate raw base metrics
            num_skills = np.random.randint(3, 25)
            years_experience = round(np.random.exponential(scale=4.0), 1)
            # Clip experience to sane range
            years_experience = min(years_experience, 22.0)
            
            # Education level mapping (0: High school, 1: Bachelors, 2: Masters, 3: PhD)
            education_level = np.random.choice([0, 1, 2, 3], p=[0.05, 0.65, 0.25, 0.05])
            
            num_certifications = np.random.choice([0, 1, 2, 3, 4], p=[0.4, 0.3, 0.15, 0.1, 0.05])
            num_projects = np.random.randint(0, 8)
            
            has_github = np.random.choice([0, 1], p=[0.4, 0.6])
            has_linkedin = np.random.choice([0, 1], p=[0.2, 0.8])
            
            gpa = round(np.random.uniform(2.2, 4.0), 2)
            
            # Match similarities compared to job descriptions
            skill_match_pct = round(np.random.uniform(10.0, 95.0), 1)
            keyword_coverage = round(np.random.uniform(5.0, 80.0), 1)
            
            # Semantic alignment from Sentence-Transformers
            semantic_similarity = round(np.random.uniform(0.1, 0.9), 3)
            tfidf_similarity = round(semantic_similarity * np.random.uniform(0.6, 0.9), 3)
            
            # Rules-based suitability logic with noise
            # Standard recruitment suitability rule
            suitability_score = (
                (skill_match_pct * 0.40) + 
                (min(years_experience / 8.0, 1.0) * 20.0) + 
                (education_level * 10.0) + 
                (semantic_similarity * 20.0) +
                (num_projects * 2.0)
            )
            
            # Map score to label
            if suitability_score >= 68.0:
                label = 'highly_suitable'
            elif suitability_score >= 50.0:
                label = 'suitable'
            elif suitability_score >= 32.0:
                label = 'average'
            else:
                label = 'not_suitable'
                
            # Inject noise: flip labels randomly for 5% of dataset to make it feel natural
            if np.random.random() < 0.05:
                label = np.random.choice(['highly_suitable', 'suitable', 'average', 'not_suitable'])
                
            data.append({
                'num_skills': num_skills,
                'years_experience': years_experience,
                'education_level': int(education_level),
                'num_certifications': num_certifications,
                'num_projects': num_projects,
                'has_github': int(has_github),
                'has_linkedin': int(has_linkedin),
                'gpa': gpa,
                'skill_match_pct': skill_match_pct,
                'keyword_coverage': keyword_coverage,
                'semantic_similarity': semantic_similarity,
                'tfidf_similarity': tfidf_similarity,
                'suitability_label': label
            })
            
        df = pd.DataFrame(data)
        return df

    def save_dataset(self, df, filepath):
        """Save dataset to CSV format."""
        os.makedirs(os.path.dirname(filepath), exist_ok=True)
        df.to_csv(filepath, index=False)
        return filepath
